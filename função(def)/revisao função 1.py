'''
faça uma função que verifica se um número é positivo ou negativo e retorne True e False.
'''
def conferir_True(numero):
    if numero >= 0:
        print("Esse número é positivo")
        print(True)
    else:
        print("Esse número é negativo")
        print(False)
        
conferir_True(2)
